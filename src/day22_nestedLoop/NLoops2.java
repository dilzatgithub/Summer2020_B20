package day22_nestedLoop;

public class NLoops2 {
    public static void main(String[] args) {

        for (int i = 1; i<= 5;i++){
            System.out.print(i+" ");
            for (int b = 1; b<= 5;b++){
                System.out.print(b+" ");
            }
            System.out.println();
        }






    }
}
